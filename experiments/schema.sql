-- ── Scenario 1: Lost Update ──────────────────────────────────────────────────
-- 재고 row를 읽고 계산 후 write → 두 트랜잭션이 동시에 같은 연산을 수행
CREATE TABLE exp_stock (
    id  BIGINT PRIMARY KEY,
    qty INT    NOT NULL
);

-- ── Scenario 2: Range Check Then Insert ─────────────────────────────────────
-- 발급 가능 여부를 SELECT 로 확인한 뒤 INSERT → 동시 확인 후 초과 발급 재현
CREATE TABLE exp_coupon (
    id          BIGINT PRIMARY KEY,
    total_qty   INT NOT NULL,
    issued_qty  INT NOT NULL
);

-- MySQL
CREATE TABLE exp_user_coupon (
    id          BIGINT AUTO_INCREMENT PRIMARY KEY,   -- PostgreSQL: BIGSERIAL / H2: GENERATED BY DEFAULT AS IDENTITY
    member_id   BIGINT NOT NULL,
    coupon_id   BIGINT NOT NULL,
    UNIQUE KEY uk_mc (member_id, coupon_id)          -- PostgreSQL/H2: UNIQUE (member_id, coupon_id)
);

-- ── Scenario 3: Write Skew ───────────────────────────────────────────────────
-- "최소 1명은 on_call=true" 불변식 → 두 트랜잭션이 각자 다른 row 를 false 로 변경
CREATE TABLE exp_duty (
    id        BIGINT  PRIMARY KEY,
    member_id BIGINT  NOT NULL,
    on_call   BOOLEAN NOT NULL
);

